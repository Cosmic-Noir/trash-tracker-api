CREATE TABLE tt_users (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    username  TEXT NOT NULL,
    date_joined TIMESTAMP DEFAULT now() NOT NULL,
    email TEXT NOT NULL,
    password  TEXT NOT NULL,
    score INTEGER DEFAULT 0 NOT NULL,
)

CREATE TABLE tt_sites (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    postedBy INTEGER REFERENCES tt_users(id) NOT NULL,
    date_posted TIMESTAMP DEFAULT now() NOT NULL,
    title TEXT NOT NULL,
    address TEXT NOT NULL,
    stateAbr TEXT NOT NULL,
    beforeImg TEXT NOT NULL,
    afterImg TEXT DEFAULT "" NOT NULL,
    description TEXT NOT NULL,
    clean BOOLEAN DEFAULT false NOT NULL
)

CREATE TABLE tt_comments (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    userRef INTEGER REFERENCES tt_users(id) NOT NULL,
    siteId INTEGER REFERENCES tt_sites(id) NOT NULL,
    date_posted TIMESTAMP DEFAULT now() NOT NULL,
    content TEXT NOT NULL

)

CREATE TABLE tt_score (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    score INTEGER DEFAULT 0 NOT NULL
)